public class Worker {
    String name;
    Double payMonth;
    Double payHour;
    Double payHollyday;
    Boolean post;

    public Worker() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Double getPayMonth() {
        return payMonth;
    }

    public void setPayMonth(Double payMonth) {
        this.payMonth = payMonth;
    }

    public Double getPayHour() {
        return payHour;
    }

    public void setPayHour(Double payHour) {
        this.payHour = payHour;
    }

    public Double getPayHollyday() {
        return payHollyday;
    }

    public void setPayHollyday(Double payHollyday) {
        this.payHollyday = payHollyday;
    }

    public Boolean getPost() {
        return post;
    }

    public void setPost(Boolean post) {
        this.post = post;
    }
}
